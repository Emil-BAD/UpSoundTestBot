class ParserService:
    """Тексты и вспомогательные данные для интерфейса бота."""

    def build_start_message(self, user_name: str) -> str:
        return (
            f"Привет, {user_name}!\n\n"
            "Я помогаю получать сведения о треках по ссылке на Яндекс Музыку: "
            "название, исполнитель и длительность.\n\n"
            "Нажми «Трек», пришли ссылку на страницу трека — и я верну данные."
        )

    def build_track_instruction_message(self) -> str:
        """Инструкция и пример ответа после нажатия кнопки «Трек»."""
        return (
            "<b>Как получить информацию о треке</b>\n\n"
            "1. Открой трек в Яндекс Музыке.\n"
            "2. Скопируй ссылку на страницу трека (формат: …/album/…/track/…).\n"
            "3. Отправь ссылку сюда одним сообщением.\n\n"
            "<b>Пример ответа бота:</b>\n"
            "<b>Информация о треке</b>\n"
            "Название: Название трека\n"
            "Исполнитель: Исполнитель\n"
            "Длительность: 3:45"
        )
